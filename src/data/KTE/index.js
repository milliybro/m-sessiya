const parseQuestions = (data) => {
  const questions = data.trim().split("\n\n");
  return questions.map((block) => {
    const lines = block.split("\n").filter(Boolean);
    const question = lines[0].replace("?", "").trim();
    const answers = lines
      .slice(1)
      .map((line) => line.replace(/[+-]/, "").trim());
    return {
      question,
      answer1: answers[0],
      answer2: answers[1],
      answer3: answers[2],
      answer4: answers[3],
    };
  });
};

